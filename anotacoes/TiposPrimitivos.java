package anotacoes;

public class TiposPrimitivos{
    public static void numericosInteiros(String[] args){
        //Tipos numéricos inteiros
        
        // byte
        byte myByte = 122;

        //short 
        short myShort = 23000;

        //int
        int myInt = 56789;

        //long
        long myLong = 45346654;
    }

    public static void numericosFlutuantes(String[] args){
        //Tipos numéricos flutuantes

        //float
        float myFloat = 3.14f;

        //double 
        double myDouble = 45.689;
    }

    public static void booleano(String[] args){
        //Booleano
        boolean myBool = true;

        boolean myOtherBool = false;
    }

    public static void caracteres(String[] args){
        //char: um caractere unicode
        char myChar = 'B';

        // String: conjunto de caracteres
        String myString = "Breno";
    }
}